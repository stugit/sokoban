export const TOTAL_LEVELS = LEVELS.length;

export function getUnlockedCount() {
  return parseInt(localStorage.getItem('sokoban_reached') ?? '0');
}

export function unlockNext(completedIndex) {
  const current = getUnlockedCount();
  if (completedIndex >= current) {
    localStorage.setItem('sokoban_reached', completedIndex + 1);
  }
}

export function isLevelUnlocked(index) {
  return index === 0 || index <= getUnlockedCount();
}

export function isLevelCompleted(index) {
  const completed = JSON.parse(localStorage.getItem('sokoban_completed') ?? '[]');
  return completed.includes(index);
}

export function markCompleted(index) {
  const completed = JSON.parse(localStorage.getItem('sokoban_completed') ?? '[]');
  if (!completed.includes(index)) {
    completed.push(index);
    localStorage.setItem('sokoban_completed', JSON.stringify(completed));
  }
  unlockNext(index);
}

export function resetProgress() {
  localStorage.removeItem('sokoban_reached');
  localStorage.removeItem('sokoban_completed');
}
